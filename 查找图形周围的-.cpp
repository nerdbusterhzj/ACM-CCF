#include<stdio.h>
#include<string.h>
int main()
{
	char a[6][9];
	memset(&a,'-',54);
	a[0][2]='*';
	a[0][3]='*';
	a[0][5]='*';
	a[0][6]='*';
	a[1][1]='*';
	a[1][3]='*';
	a[1][4]='*';
	a[1][5]='*';
	a[1][6]='*';
	a[1][7]='*';
	a[2][1]='*';
	a[2][6]='*';
	a[2][7]='*';
	a[3][2]='*';
	a[3][5]='*';
	a[3][6]='*';
	a[4][3]='*';
	a[4][4]='*';
	a[4][5]='*';
	a[5][4]='*';
	int n,k,m=0;
	scanf("%d",&n);
	if(n<0||n>8) printf("Input Error"); 
	else 
	{
		for(int i=0;i<6;i++)
		{
			for(int j=0;j<9;j++)
			{
				if(a[i][j]=='*')
				{
					k=0;
					if(a[i-1][j-1]=='-') k++;	
					if(a[i-1][j]=='-') k++;
					if(a[i-1][j+1]=='-') k++;
					if(a[i][j-1]=='-') k++;
					if(a[i][j+1]=='-') k++;
					if(a[i+1][j-1]=='-') k++;
					if(a[i+1][j]=='-') k++;
					if(a[i+1][j+1]=='-') k++;
					if(k==n) m++;
				}
			}
		}
		printf("%d",m);		
	}
}
	
	
	
